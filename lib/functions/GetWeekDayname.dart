String GetWeekDayName(int day) {
  switch (day) {
    case 1:
      return "Sunday";
      break;
    case 2:
      return "Monday";
      break;
    case 3:
      return "Tuesday";
      break;
    case 4:
      return "Wednesday";
      break;
    case 5:
      return "Friday";
      break;
    case 1:
      return "Saturday";
      break;
   
  }
  return "";
}
